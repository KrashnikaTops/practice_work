#52.How Many Basic Types Of Functions Are Available In Python?  

#ans:-

#1.Built-in Functions: These are functions provided by Python itself, such as len(), print(), type(), max(), and min(). 
                    #-They are readily available for use without needing to import any libraries.


#2.Lambda Functions: These are small anonymous functions defined with the lambda keyword. 
                  #-They are often used for short, throwaway functions. 

                  #example: square = lambda x: x ** 2
                  
#3.User-Defined Functions: These are functions that you create yourself using the def keyword.  
                         
                






